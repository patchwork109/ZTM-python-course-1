# format -> for variable in iterable:

# iterable -> list, dictionary, tuple, set, string
# iterate -> check each item in the collection one by one

for item in [1,2,3,4,5]:
    print(item)

for character in 'Sam':
    print(character)

for item in (1,2,3,4,5):
    for letter in ['a', 'b', 'c', 'd']:
        print(item, letter)